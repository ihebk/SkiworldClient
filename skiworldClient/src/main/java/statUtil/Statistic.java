package statUtil;

public class Statistic {
	private int y;
	private String x;
	
	public Statistic() {
		super();
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	
	
}
